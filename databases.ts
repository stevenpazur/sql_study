/*
Transactional databases - lots of changes happening 
Analytical databases - mainly used for analytical purposes.  No changes.

Databases give us a way to store data in a server we can access.

Table - a 2-dimensional data structure organized into rows and columns
Rows: data about an instance
Columns: categories of data

Relational - related data in a logical way.

Many-to-one: Favorite food - cat

Primary key - unique identifying column that every table requires.  Identifying column or columns.
Foreign key - a reference to other or really relational table data, generally by the primary key.

Constraints are primary key and foreign key.  
1. Must be unique - must have unique values in the table.
2. Not null
*/